#pragma pack(push, 1)
struct InstructionPackage
{
	InstructionType m_Type;
	OperandPackage m_Operands[4];
};
#pragma pack(pop)

#pragma pack(push, 1)
struct FullRedirectionPackage
{
	uint16_t m_BaseIndex;
};
#pragma pack(pop)

#ifdef INSTRUCTION_PACKING
#pragma pack(push, INSTRUCTION_PACKING)
#else
#pragma pack(push, 1)
#endif
struct Package
{
	PackageType m_Type;
	union
	{
		PrefixPackage m_Prefix;
		InstructionPackage m_Instruction;
		RedirectionPackage m_Redirection;
		FullRedirectionPackage m_FullRedirection;
	};
};
